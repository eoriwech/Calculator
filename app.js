//performs addition of two numbers
function add(num1, num2) {
    let result = num1 + num2;
    return result;
}

//Subtraction of two numbers
function subtract(num1, num2) {
    let result = num1 - num2;
    return result;
}

//Multiplication of two numbers
function multiply(num1, num2) {
    let result = num1 * num2;
    return result;
}
